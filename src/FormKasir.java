    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try {

        double harga =
                Double.parseDouble(txtHarga.getText());

        int qty =
                Integer.parseInt(txtJumlah.getText());

        double subtotal =
                harga * qty;

        double diskon =
                Double.parseDouble(txtDiskon.getText());

        double potongan =
                subtotal * diskon / 100;

        double setelahDiskon =
                subtotal - potongan;

        double pajak =
                setelahDiskon * 0.11;

        double total =
                setelahDiskon + pajak;

        txtSubtotal.setText(
                String.valueOf(subtotal));

        txtPajak.setText(
                String.valueOf(pajak));

        txtTotal.setText(
                String.valueOf(total));

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if(txtHarga.getText().isEmpty()
        || txtJumlah.getText().isEmpty()
        || txtSubtotal.getText().isEmpty()) {

    JOptionPane.showMessageDialog(
            this,
            "Klik Hitung terlebih dahulu");

    return;
}
        
        DefaultTableModel model =
            (DefaultTableModel) tblKasir.getModel();

    int idBarang =
            getIdBarang(
                    cmbBarang.getSelectedItem().toString());

    String namaBarang =
            cmbBarang.getSelectedItem().toString();

    double harga =
            Double.parseDouble(txtHarga.getText());

    int qty =
            Integer.parseInt(txtJumlah.getText());

    double subtotal =
            Double.parseDouble(txtSubtotal.getText());
    txtJumlah.setText("");
txtSubtotal.setText("");

cmbBarang.requestFocus();

    model.addRow(new Object[]{
        idBarang,
        namaBarang,
        harga,
        qty,
        subtotal
    });
    hitungTotalKeseluruhan();
    }//GEN-LAST:event_btnTambahActionPerformed

