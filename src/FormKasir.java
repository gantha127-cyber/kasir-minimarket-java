    private void hitungTotalKeseluruhan() {
        double total = 0;

    for(int i = 0; i < tblKasir.getRowCount(); i++) {

        Object nilai =
                tblKasir.getValueAt(i, 4);

        if(nilai != null) {

            total += Double.parseDouble(
                    nilai.toString());

        }

    }

    txtTotal.setText(
            String.valueOf(total));

}
    
    private int getIdBarang(String namaBarang) {

    int id = 0;

    try {

        Connection conn = Koneksi.getConnection();

        String sql =
                "SELECT id_barang FROM barang WHERE nama_barang=?";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, namaBarang);

        ResultSet rs =
                pst.executeQuery();

        if(rs.next()) {

            id = rs.getInt("id_barang");

        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }

    return id;

}
    
    private void loadMember() {

    try {

        Connection conn =
                Koneksi.getConnection();

        Statement st =
                conn.createStatement();

        ResultSet rs =
                st.executeQuery("SELECT * FROM member");

        cmbMember.removeAllItems();

        while(rs.next()) {

            cmbMember.addItem(
                    rs.getString("nama_member"));

        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }

}

