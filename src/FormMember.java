    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if(txtIdMember.getText().isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Pilih data yang akan diubah");

        return;
    }

    try {

        Connection conn = Koneksi.getConnection();

        String sql =
                "UPDATE member SET nama_member=?, no_hp=?, Diskon=? WHERE id_member=?";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, txtNamaMember.getText());
        pst.setString(2, txtNoHp.getText());
        pst.setString(3, txtDiskon.getText());
        pst.setString(4, txtIdMember.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Data Berhasil Diubah");

        tampilData();

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }

    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         if(txtIdMember.getText().isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Pilih data yang akan dihapus");

        return;
    }

    int jawab = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if(jawab == JOptionPane.YES_OPTION) {

        try {

            Connection conn = Koneksi.getConnection();

            String sql =
                    "DELETE FROM member WHERE id_member=?";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setString(1, txtIdMember.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(
                    this,
                    "Data Berhasil Dihapus");

            tampilData();

        } catch(Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());

        }

    }
    }//GEN-LAST:event_btnHapusActionPerformed

