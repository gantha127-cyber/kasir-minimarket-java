    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(txtNamaMember.getText().trim().isEmpty()
            || txtNoHp.getText().trim().isEmpty()
            || txtDiskon.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Semua data harus diisi");

        return;
    }

    try {

        Connection conn = Koneksi.getConnection();

        String sql =
                "INSERT INTO member(nama_member,no_hp,Diskon) VALUES(?,?,?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, txtNamaMember.getText());
        pst.setString(2, txtNoHp.getText());
        pst.setDouble(
      3,
      Double.parseDouble(txtDiskon.getText())
        );

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Data Berhasil Disimpan");

        tampilData();

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

