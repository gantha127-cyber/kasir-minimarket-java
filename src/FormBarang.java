    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if(txtIdBarang.getText().isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Pilih data yang akan dihapus!");

        return;
    }

    int jawab = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if(jawab == JOptionPane.YES_OPTION) {

        try {

            Connection conn = Koneksi.getConnection();

            String sql =
                    "DELETE FROM barang WHERE id_barang=?";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setString(1, txtIdBarang.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(
                    this,
                    "Data berhasil dihapus");

            tampilData();

        } catch(Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());

        }

    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
       if(txtNamaBarang.getText().trim().isEmpty()
            || txtHarga.getText().trim().isEmpty()
            || txtStok.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Semua data harus diisi");

        return;
    }

    try {

        Connection conn = Koneksi.getConnection();

        int idKategori =
                getIdKategori(
                        cmbKategori.getSelectedItem().toString());

        String sql =
                "INSERT INTO barang(nama_barang,id_kategori,harga,stok) VALUES(?,?,?,?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(
                1,
                txtNamaBarang.getText());

        pst.setInt(
                2,
                idKategori);

        pst.setDouble(
                3,
                Double.parseDouble(txtHarga.getText()));

        pst.setInt(
                4,
                Integer.parseInt(txtStok.getText()));

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                this,
                "Data Berhasil Disimpan");

        tampilData();

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }
 
    }//GEN-LAST:event_btnSimpanActionPerformed

