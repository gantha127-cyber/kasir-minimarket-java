    private int getIdKategori(String namaKategori) {

        int id = 0;

        try {

            Connection conn = Koneksi.getConnection();

            String sql =
                    "SELECT id_kategori FROM kategori WHERE nama_kategori=?";

            PreparedStatement pst =
                    conn.prepareStatement(sql);

            pst.setString(1, namaKategori);

            ResultSet rs =
                    pst.executeQuery();

            if (rs.next()) {

                id = rs.getInt("id_kategori");

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());

        }

        return id;
    }

    private void loadKategori() {

        try {

            Connection conn = Koneksi.getConnection();

            Statement st = conn.createStatement();

            ResultSet rs =
                    st.executeQuery("SELECT * FROM kategori");

            cmbKategori.removeAllItems();

            while (rs.next()) {

                cmbKategori.addItem(
                        rs.getString("nama_kategori"));

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());

        }

    }

    private void tampilData() {

        DefaultTableModel model =
                new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Nama Barang");
        model.addColumn("Kategori");
        model.addColumn("Harga");
        model.addColumn("Stok");

        try {

            Connection conn =
                    Koneksi.getConnection();

            String sql =
                    "SELECT barang.id_barang, " +
                    "barang.nama_barang, " +
                    "kategori.nama_kategori, " +
                    "barang.harga, " +
                    "barang.stok " +
                    "FROM barang " +
                    "JOIN kategori " +
                    "ON barang.id_kategori = kategori.id_kategori";

            Statement st =
                    conn.createStatement();

            ResultSet rs =
                    st.executeQuery(sql);

            while (rs.next()) {

                model.addRow(new Object[]{
                    rs.getString("id_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("nama_kategori"),
                    rs.getString("harga"),
                    rs.getString("stok")
                });

            }

            tblBarang.setModel(model);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());

        }

    }
